# para printar uma contagem utiliza-se
for c in range(0, 6):
    print(c)
print('FIM')
for c in range(1, 7):
    print(c)
print('Boom')
# para a contagem ser ao contrário utiliza-se o -1 no final
for c in range(6, 0, -1):
    print(c)
print('Kaboom')
# para printar pulando números
for c in range(0, 7, 2):
    print(c)
print('Números pares')