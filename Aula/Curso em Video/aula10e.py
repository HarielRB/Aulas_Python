print('Calculadora de média escolar')
n1 = float(input('Insira sua primeira nota:'))
n2 = float(input('Insira sua segunda nota:'))
m = (n1 + n2)/2
print('Sua média é {}'.format(m))
if m <6:
    print('Sua nota está abaixo da média necessária ;-;')
else:
    print('Parabéns, Você alcançou a média :)')
